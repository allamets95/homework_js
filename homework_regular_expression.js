// Має принаймні 6 символів.
// Не містить літери "A" або "a" (великої або малої), а також кириличних "А" або "а".
const regex = /^(?!.*[AaАа]).{6,}$/;
/*
    ^ -> Початок рядка.
    (?!.*[AaАа]) -> Негативний погляд уперед, який перевіряє, що в рядку немає жодної з літер "A", "a" (Латиниця), "А" або "а" (Кирилиця).
    .{6,} -> Будь-які символи, принаймні 6 символів.
    $ -> Кінець рядка.
*/
const testStrings = [
    "demo",  // підходить
    "0123456",  // підходить
    "012345A",  // не підходить (містить "A")
    "abcde",   // не підходить (менше 6 символів)
    "1234567", // підходить
    "Привіт",  // не підходить (менше 6 символів)
    "Привет",  // підходить
    "ПривАт",  // не підходить (містить "А")
    "Приват",  // не підходить (містить "а")
    "11335а",  // не підходить (містить "а")
];

testStrings.forEach(str => {
    console.log(`${str}: ${regex.test(str)}`);
});